<script>

const blogContainer = document.getElementById("card");
const loadMoreBtn = document.getElementById("loadMoreBtn");

const postsPerPage = 3;
let visiblePosts = postsPerPage;

function loadMore() {
 const blogPosts = document.querySelectorAll(".x");

  for (let i = visiblePosts; i < visiblePosts + postsPerPage; i++) {
      if (i >= blogPosts.length) {
      loadMoreBtn.style.display = "none"; // Hide button when all posts are loaded
      break;
      }

      blogPosts[i].style.display = "block"; // Display hidden posts
  }

  visiblePosts += postsPerPage;
  
  // Check if there are remaining posts to show
  if (visiblePosts >= blogPosts.length) {
      loadMoreBtn.style.display = "none"; // Hide button when no more posts are left
  }
  }

  loadMoreBtn.addEventListener("click", loadMore);

  // Initially hide extra posts
  const blogPosts = document.querySelectorAll(".x");
  for (let i = visiblePosts; i < blogPosts.length; i++) {
  blogPosts[i].style.display = "none";
  }

  // Check if "Load More" button should be hidden initially
  if (blogPosts.length <= visiblePosts) {
  loadMoreBtn.style.display = "none";
  }
</script>
